#include <iostream>
#include <fstream>
#include <cmath>
using namespace std;
///////////////////////////////////////////////////////////////////////////////
#define X_min -10
#define X_max 10
#define N     1000

///////////////////////////////////////////////////////////////////////////////
double h = (double)(X_max-X_min)/(N-1);
double x[N];
double data[N];
fstream plik_out;

///////////////////////////////////////////////////////////////////////////////
double My_function(double x){
  double y = sin(x)*x;
  return y;
}

///////////////////////////////////////////////////////////////////////////////
int main(){
  plik_out.open("out.data", ios::out);

  for(int i=0; i<N; i++){
    x[i] = X_min + i*h;
    data[i] = My_function( x[i] );
  }

  for(int i=0; i<N; i++){
    plik_out << x[i]    << "   " 
	     << data[i] << "   "
	     << endl;
  }
  
  return 0;
}

